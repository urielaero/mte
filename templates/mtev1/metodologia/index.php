				<h3>¿Cómo calculamos las calificaciones de matemáticas y español de una escuela?</h3>
				<p>La metodología para calcular las calificaciones de matemáticas y español de una escuela se basa, estrictamente en los resultados de la prueba ENLACE.</p>
				
				<p><b>Escuelas primarias:</b></p>
				<p>Para calcular el resultado de matemáticas de una escuela primaria se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(Calificación de matemáticas de tercero de primaria) + (calificación de matemáticas de cuarto de primaria) + (calificación de matemáticas de quinto de primaria) + (calificación de matemáticas de sexto de primaria) / 4  = calificación global de matemáticas de la escuela.</p>
				<p>Para calcular el resultado de español de una escuela primaria se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(Calificación de español de tercero de primaria) + (calificación de español de cuarto de primaria) + (calificación de español de quinto de primaria) + (calificación de español de sexto de primaria) / 4  = calificación global de español de la escuela.</p>
				<p>Para calcular el resultado global de la escuela primaria se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(calificación global de matemáticas de la escuela) + (calificación global de español de la escuela) / 2 = calificación global de la escuela primaria</p>
				
				<p><b>Escuelas secundarias:</b></p>
				<p>Para calcular el resultado de matemáticas de una escuela secundaria a se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(calificación de matemáticas de primero de secundaria) + (calificación de matemáticas de segundo de secundaria) + (calificación de matemáticas de tercero de secundaria) / 3 = calificación global de matemáticas de la escuela</p>
				<p>Para calcular el resultado de español de una escuela primaria se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(calificación de español de primero de secundaria) + (calificación de español de segundo de secundaria) + (calificación de tercero de secundaria) / 3 =  calificación global de español de la escuela</p>
				<p>Para calcular el resultado global de la escuela primaria se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(calificación global de matemáticas de la escuela) + (calificación global de español de la escuela) / 2 = calificación global de la escuela secundaria</p>

				<p><b>Bachilleratos:</b></p>
				<p>Para calcular el resultado de matemáticas de una escuela de bachillerato o técnica profesional se toma el único resultado disponible:</p>
				<p class='indent'>(calificación de matemáticas de tercero de bachillerato) = calificación global de matemáticas de la escuela.</p>
				<p>Para calcular el resultado de español de una escuela de bachillerato o técnica profesional se toma el único resultado disponible:</p>
				<p class='indent'>(calificación de español de tercero de bachillerato) = calificación de global de español de la escuela.</p>
				<p>Para calcular el resultado global de la escuela de bachillerato o técnica profesional se lleva a cabo el siguiente promedio:</p>
				<p class='indent'>(calificación global de matemáticas de la escuela) + (calificación global de español de la escuela) / 2 = calificación global de la escuela de bachillerato</p>

