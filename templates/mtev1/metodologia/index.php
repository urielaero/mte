				<h3>¿Dónde obtiene su información Mejora tu escuela?</h3>
				<p>Mejora tu escuela usa información de tres distintas bases de datos de la Secretaría de Educación Pública.</p>
				<p>La Evaluación nacional del logro académico en centros escolares, conocida también como prueba ENLACE.    Disponible en: www.enlace.sep.gob.mx</p>
				<p>El Sistema nacional de información educativa o SNIE.  Disponible en: www.snie.sep.gob.mx</p>
				<p>El Registro nacional de alumnos, maestros y alumnos o RNAME.  Disponible en: www.rname.sep.gob.mx</p>
				<p>Mejora tu escuela también ha creado una base de datos adicional con información cualitativa de escuelas que ha obtenido por Internet y por encuestas enviadas a correos electrónicos de directores de escuelas en todo México.</p>
