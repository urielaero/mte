				<h3>¿Qué quiere decir el triángulo negro junto al nombre de mi escuela?</h3>
				<p>El triángulo negro junto al nombre de una escuela indica que la escuela no administra la prueba ENLACE a sus alumnos.   Por lo tanto, no tenemos manera de obtener información para definir su calidad educativa.</p>
			</div>
