				<h3>¿Qué quiere decir el símbolo de precaución junto al nombre de mi escuela?</h3>
				<p>La prueba ENLACE utiliza tecnologías avanzadas para garantizar el control de calidad de los resultados individuales y por grupo.</p>
 				<p>ENLACE identifica como “copia u otras acciones inaceptables” a cualquier tendencia que indique la existencia de copia, dictado de respuestas y otras prácticas fraudulentas. </p>
				<p>El símbolo de precaución junto al nombre de una escuela indica uno de dos posibles escenarios:</p>
				<ul>
					<li>Más de 10% de las pruebas de administradas en la escuela se consideran “no confiables” o “fraudulentas” por la SEP.</li>
					<li>Menos de 85% de los alumnos tomaron la prueba.   Esta estadística potencialmente sugiere que la escuela sólo permite que sus mejores alumnos tomen la prueba.    Este porcentaje se calcula de la siguiente manera:  (número de alumnos que tomaron la prueba) / (número de niños inscritos en la escuela) x 100=  porcentaje de alumnos que toman la prueba.</li>
				</ul>
