				<a name='pregunta2'></a>
				<h3>¿Qué quiere decir el triángulo gris junto al nombre de mi escuela?</h3>
				<p>El triángulo gris indica que no todos los grados de la escuela toman la prueba ENLACE.   Para los años que no tenemos resultado se asigna un cero, por lo tanto, el promedio de la escuela tiende a ser muy bajo.</p>
				<p>Esta situación se puede dar en dos ocasiones:</p>
				<ul>
					<li>La escuela toma la decisión de que uno de sus grados no tome la prueba ENLACE.</li>
					<li>La escuela es nueva y aún no tiene estudiantes en todos los grados.</li>
				</ul>
				<p>Para conocer los resultados por grado de la escuela por favor visita la sección de “Resultados educativos” en el perfil de la escuela.</p>

				
